<h2>Backup</h2>
<p>1) <a href="http://localhost/phpmyadmin/index.php?route=/database/export&db=biblio" target="_blank">Aller ici</a> et cliquer sur "Exporter"</p>
<p>Enregistrer le fichier dans c:/xampp/htdocs/Bibliotheque/models</p>
<p>2) Dans le programme Visual Studio Code, dans le terminal en bas : </p>
<p>Remonter avec la flèche vers le haut jusqu'à :</p>
<code>git add .</code>
<p>Appuyer sur Enter</p>
<p>Remonter avec la flèche vers le haut jusqu'à :</p>
<code>git commit -m "backup"</code>
<p>Appuyer sur Enter</p>
<p>Remonter avec la flèche vers le haut jusqu'à : </p>
<code>git push -u origin main</code>
<p>Appuyer sur Enter</p>
<p>Et voilà :D</p>